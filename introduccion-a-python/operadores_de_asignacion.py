'''
Operadores de asignacion o de incremento
'''

#para estos tipos de operadores devemos de asignarle el valor incial
#en este caso cero, ya que queremos arancar del valor cero, es obligatorio indicar
#de donde iniciar nuestras operaciones de asignacion
c=0
# y despues el incremento que queremos que haga
c=c+10
#tambien lo podemos poner asi el incremento para que se vea mas pulcro el codigo
# c+=1
#ahora vamos a restarle -5 al incremento
c-=5
#ahora restando ese incrimento vamos a multiplicarlo por 3
c *=3
#ahora vamos a dividir ese incremento entre 5
c /=5
#ahora vamos a elevarlo a la potencia de 3 **3
c **=3
#ahora le sacamos el porsentaje de 3
c %=3
print(c)